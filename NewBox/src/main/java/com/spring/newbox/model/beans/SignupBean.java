package com.spring.newbox.model.beans;

public class SignupBean {
 private String signup_id;
 private String signup_pw;
 private String signup_name;
 private String signup_email;
 
	public String getSignup_id() {
		return signup_id;
	}
	public void setSignup_id(String signup_id) {
		this.signup_id = signup_id;
	}
	public String getSignup_pw() {
		return signup_pw;
	}
	public void setSignup_pw(String signup_pw) {
		this.signup_pw = signup_pw;
	}
	public String getSignup_name() {
		return signup_name;
	}
	public void setSignup_name(String signup_name) {
		this.signup_name = signup_name;
	}
	public String getSignup_email() {
		return signup_email;
	}
	public void setSignup_email(String signup_email) {
		this.signup_email = signup_email;
	}
}
